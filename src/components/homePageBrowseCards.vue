<script>
import { Loading, QSpinnerBars, colors } from 'quasar';
import axios from 'axios';

axios.defaults.baseURL = process.env.BASE_URL;
axios.defaults.headers.get.Accepts = 'application/json';
axios.defaults.headers.common['Access-Control-Allow-Origin'] = '*';
axios.defaults.headers.common['Access-Control-Allow-Headers'] = 'Origin, X-Requested-With, Content-Type, Accept';

export default {
  components: {
  },
  name: 'statusComponent',
  data() {
    return {
      eventTypeOptions: [],
      eventType: [],
      indices: [11, 12, 13, 14, 16, 23],
      selected: null,
      primary: '',
      imgs: '',
      visible: false,
    };
  },
  mounted() {
    this.primary = colors.getBrand('primary');
    axios.defaults.headers.Authorization = `Bearer ${this.$q.sessionStorage.getItem(
      'login-token',
    )}`;

    Loading.show({
      spinner: QSpinnerBars,
      spinnerColor: 'primary',
      thickness: '3',
    });

    axios
      .get('/api/eventSystem/eventType')
      .then((Response) => {
        Loading.hide();
        this.eventTypeOptions = Response.data.data;
      })
      .catch((e) => {
        Loading.hide();
        if (e.message === 'Request failed with status code 401') {
          this.$q.sessionStorage.remove('login-token');
          this.$q.sessionStorage.set('login-token', null);
          this.$router.push('/login');
        }

        this.$q.notify({
          color: 'red-5',
          textColor: 'white',
          icon: 'error',
          message: e.message,
          position: 'top',
        });
      });
  },
  methods: {
    showCard(src) {
      this.imgs = src;
      this.visible = true;
    },
    handleHide() {
      this.visible = false;
    },
  },
};
</script>
